package DataTypesinjava;

public class DatatypeSiZE {
    public static void main(String[] args) {
        System.out.println(Short.SIZE);// short datatype stored in 16 bits
        System.out.println(Float.SIZE/8);// short datatype memory stored in 2 bytes
        System.out.println(Character.SIZE/8);

        // range in Integer data types

        System.out.println(Integer.MAX_VALUE);
        System.out.println(Integer.MIN_VALUE);

        // range in Short data ype
        System.out.println(Long.MAX_VALUE);
        System.out.println(Long.MIN_VALUE);

        // range in Float data type
        System.out.println(Float.MAX_VALUE);
        System.out.println(Float.MIN_VALUE);

        // range in Double data type
        System.out.println(Double.MAX_VALUE);
        System.out.println(Double.MIN_VALUE);

        //range in byte data type
        System.out.println(Byte.MAX_VALUE);
        System.out.println(Byte.MIN_VALUE);
    }
}
