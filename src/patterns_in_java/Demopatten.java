package patterns_in_java;

public class Demopatten {

	public static void main(String[] args) {
		
		for(int row=0;row<5;row++) 
		{
			for(int col=0;col<5;col++) 
			{
				System.out.print(row+1 + " ");
			}
			System.out.println();
		}

	}

}
